# Affin boşluq cəriməsi ilə qlobal hizalama
# Implement global alignment using Affine Gap Penalties
# gap open = 11, gap extend = 1
def global_alignment_affine(s1, s2):
    gap_open = 11
    gap_extend = 1
    n, m = len(s1), len(s2)
    
    # 3 DP matrisi: Lower (gap in s2), Middle (match/mismatch), Upper (gap in s1)
    lower = [ [-float('inf')] * (m + 1) for _ in range(n + 1) ]
    middle = [ [-float('inf')] * (m + 1) for _ in range(n + 1) ]
    upper = [ [-float('inf')] * (m + 1) for _ in range(n + 1) ]
    
    middle[0][0] = 0
    for i in range(1, n + 1):
        lower[i][0] = -gap_open - (i - 1) * gap_extend
        middle[i][0] = lower[i][0]
    for j in range(1, m + 1):
        upper[0][j] = -gap_open - (j - 1) * gap_extend
        middle[0][j] = upper[0][j]
        
    for i in range(1, n + 1):
        for j in range(1, m + 1):
            lower[i][j] = max(
                lower[i-1][j] - gap_extend,
                middle[i-1][j] - gap_open
            )
            upper[i][j] = max(
                upper[i][j-1] - gap_extend,
                middle[i][j-1] - gap_open
            )
            match_score = BLOSUM62[s1[i-1]][s2[j-1]]
            middle[i][j] = max(
                middle[i-1][j-1] + match_score,
                lower[i][j],
                upper[i][j]
            )
            
    # Hizalanmanı geriyə izləyirik
    # Backtrack path
    align1, align2 = [], []
    i, j = n, m
    matrix_state = 'middle' # 'middle', 'lower', 'upper'
    
    while i > 0 or j > 0:
        if matrix_state == 'middle':
            if i > 0 and j > 0 and middle[i][j] == middle[i-1][j-1] + BLOSUM62[s1[i-1]][s2[j-1]]:
                align1.append(s1[i-1])
                align2.append(s2[j-1])
                i -= 1
                j -= 1
            elif middle[i][j] == lower[i][j]:
                matrix_state = 'lower'
            else:
                matrix_state = 'upper'
        elif matrix_state == 'lower':
            align1.append(s1[i-1])
            align2.append("-")
            if i > 0 and lower[i][j] == lower[i-1][j] - gap_extend:
                i -= 1
            else:
                i -= 1
                matrix_state = 'middle'
        elif matrix_state == 'upper':
            align1.append("-")
            align2.append(s2[j-1])
            if j > 0 and upper[i][j] == upper[i][j-1] - gap_extend:
                j -= 1
            else:
                j -= 1
                matrix_state = 'middle'
                
    align1.reverse()
    align2.reverse()
    
    return middle[n][m], "".join(align1), "".join(align2)

def main():
    s1, s2 = read_input()
    if not s1:
        return
    score, align1, align2 = global_alignment_affine(s1, s2)
    
    import os
    script_dir = os.path.dirname(os.path.abspath(__file__))
    with open(os.path.join(script_dir, "output.txt"), "w") as f:
        f.write(str(score) + "\n")
        f.write(align1 + "\n")
        f.write(align2 + "\n")

if __name__ == "__main__":
    main()
